                                        <p class="pb-25">
                                            <b>Obligaciones de ambas partes:</b> 				                        
                                            <br><br>
                                            Como hemos visto en notas anteriores, tanto el trabajador como el patrón/jefe tienen obligaciones a seguir ante la ley, y de no seguirse adecuadamente, puede penalizarse de acuerdo a la misma ley.				                        
                                            <br><br>
                                            Para el patrón, es importante difundir y mantener una política para prevenir riesgos psicosociales, las cuales debe contemplar: La prevención de la violencia laboral, promover un entorno de trabajo saludable y prevención de factores de riesgo. Además, el patrón deberá identificar factores que puedan poner en riesgo la salud psicológica de los trabajadores, evaluar el entorno para que sea lo más sano posible y controlar los mismos factores de riesgo previamente comentados.				                        
                                            <br><br>
                                            En caso de que los trabajadores hayan tenido una experiencia traumática durante o con motivo de trabajo, deberá ser atendido inmediatamente en el instituto de seguridad social o al médico del centro de trabajo/empresa.
                                            <br><br>
                                            También es la obligación del patrón realizar exámenes psicológicos a los trabajadores expuestos a violencia laboral, llevar un registro de los mismos y cualquier incidente/reporte que pueda suceder.
                                            <br><br>
                                            En cuanto al trabajador, este tiene la obligación de, además de obedecer esta norma, tienen la obligación de reportar cualquier conducta de los compañeros que vaya en contra de la misma.
                                            También es su obligación de identificar factores de riesgo probables y los actos de violencia que puedan suceder, y someterse a cualquier examen médico y evaluación psicológica que sirva para determinar la salud mental.
                                            <br><br>
                                            Esperamos que esta nota te sirva de guía para conocer más sobre los factores que ponen en riesgo tu salud mental y la de tus compañeros. Es importante que todos presten atención a estos factores y tomen las medidas adecuadas para crear un entorno laboral saludable y positivo. Si necesitas ayuda o asesoría para hacer de tu área laboral un lugar mejor psicológicamente, puedes contactarnos y te asesoraremos en los mejores pasos a seguir. Queda atento para próximas notas similares.
