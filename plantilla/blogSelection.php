				                        <h3 class="title pb-24">Riesgos psicosociales en el trabajo,<br> ¿qué dice la ley sobre esto?</h3>
				                        <p class="pb-25">
                                            Los factores psicosociales se refieren a los aspectos en el mundo social que pueden llegar a afectar la salud mental y emocional de las personas, ya sea en el trabajo, o en el circulo personal de una persona. De ser negativo, este factor puede repercutir gravemente en situaciones laborales, además de que pueden influir gravemente en el bienestar general de los empleados y su capacidad para realizar su trabajo de manera efectiva.
                                            <br><br>
                                            La NOM (Norma Oficial Mexicana) ha determinado varios factores que puedan suceder por cualquier motivo con el objetivo de prevenir que estos factores sucedan, promoviendo así un entorno favorable en los centros de trabajo. En esta nota te comentaremos todo lo que debes saber para detectar estos factores y para que puedas promover un ambiente psicológicamente seguro en el trabajo.
                                            <br><br>
                                            <b>¿Qué factores son los que más afectan?</b> 			                        
                                            <br><br>
                                            La ley Mexicana ha encontrado varios factores que pueden afectar a la salud mental de los trabajadores dentro y fuera del área laboral, sin embargo, los más comunes y los que más llegan a afectar son:
                                        </p>
                                        <ul class="stylelisting pb-20">
                	                        <li>1. Carga de trabajo: la cantidad de trabajo y las demandas laborales que se les exigen a los trabajadores pueden afectar su salud mental y física.</li>                	                        <li>Control y autonomía: la capacidad de los empleados para tomar decisiones y tener cierto grado de control sobre su trabajo puede tener un impacto positivo en su bienestar.</li>
                	                        <li>2. Relaciones interpersonales: las relaciones con los compañeros de trabajo y los supervisores pueden influir en el bienestar emocional de los empleados.</li>
                	                        <li>3. Apoyo social: la existencia de un sistema de apoyo social en el trabajo puede reducir el estrés y mejorar la salud mental de los trabajadores.</li>
                                            <li>4. Ambiente físico: el entorno físico en el que se realiza el trabajo también puede afectar la salud mental de los empleados.</li>
                                            <li>5. Desarrollo profesional: la posibilidad de aprender nuevas habilidades y avanzar en la carrera puede tener un impacto positivo en la motivación y la satisfacción laboral.</li>
                                            <li>6. Cultura organizacional: la cultura de la organización, incluyendo los valores y las normas, puede influir en el bienestar emocional y la motivación de los empleados.</li>
                                            <li>7. Compensación y beneficios: la remuneración y los beneficios que se ofrecen a los empleados también pueden afectar su satisfacción laboral y su bienestar emocional.</li>
                                        </ul>
